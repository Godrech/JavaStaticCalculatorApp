public class CalculatorApp {

    public static void main(String[] args) {
        double num1= 64;
        double num2 = 20;
        
        double answer1 = num1 + num2 ;
        double answer2 = num1 - num2;
        double answer3 = num1 / num2;
        double answer4 = num1 * num2;
        
        
        System.out.println(num1 + " + " + num2 +" = " + answer1);
        System.out.println(num1 + " - " + num2 +" = " + answer2);
        System.out.println(num1 + " / " + num2 +" = " + answer3);
        System.out.println(num1 + " x " + num2 +" = " + answer4);
        
    }
}
